// Function to insert sample memories (for development use)
export const insertSampleMemories = async (supabase: any, userId: string) => {
  const memoriesWithUserId = SAMPLE_OAKLAND_MEMORIES.map(memory => ({
    ...memory,
    user_id: userId
  }));

  const { data, error } = await supabase
    .from('oakland_memories')
    .insert(memoriesWithUserId)
    .select();

  if (error) {
    console.error('Error inserting sample memories:', error);
    throw error;
  }

  return data;
};
